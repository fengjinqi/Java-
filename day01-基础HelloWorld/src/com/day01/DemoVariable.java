package com.day01;

public class DemoVariable {
    /**
     * 常量 程序运行期间可以改变的量
     * 数据类型 变量名称
     * 变量名称 = 值
     * @param args
     */
    public static void main(String[] args){
        int n;
        n = 50;
        double num = 40.2;
        System.out.println(n);
        System.out.println(num);
    }
}
