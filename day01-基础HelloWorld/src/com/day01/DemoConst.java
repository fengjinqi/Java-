package com.day01;

public class DemoConst {
    /**
     * 常量：程序运行期间，固定不变的量
     * 字符串常量:用双引号的则是常量 "abc"
     * 整数常量: 直接写的数字，没有小数 1,2
     * 浮点常量:数字，有小数点 3.14
     * 字符常量 单引号单个字符 'a'
     * 布尔常量 true/false
     * 空常量 null 没有任何值
     * @param args
     */
    public static void main(String[] args){
        //字符串常量
        System.out.println("aaa");
        //整数常量
        System.out.println(444);
        //浮点数常量
        System.out.println(3.14);
        //字符常量
        System.out.println('h');//TODO 有且只能有一个字符
        //布尔
        System.out.println(true);
        System.out.println(false);
        //空

        //System.out.println(null);不能直接打印输出
    }
}
