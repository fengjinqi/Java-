package com.day01;

public class DemoDataType {
    /**
     * 数据类型转换
     *
     * @param args
     */
    public static void main(String[] args){
        /**
         * 强制类型转换
         */
        int num = (int) 100L;
        byte x =10;
        short y =40;
        int z = x + y;
        System.out.println(num);
        System.out.println(z);
        int b = 6+x+10;
        System.out.println();
    }
}
